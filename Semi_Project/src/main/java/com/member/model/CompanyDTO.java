package com.member.model;

public class CompanyDTO {
	
	private String company_id;
	private String company_pwd;
	private String company_email;
	private String company_phone;
	private String company_name;
	private String company_storenum;
	
	
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public String getCompany_pwd() {
		return company_pwd;
	}
	public void setCompany_pwd(String company_pwd) {
		this.company_pwd = company_pwd;
	}
	public String getCompany_email() {
		return company_email;
	}
	public void setCompany_email(String company_email) {
		this.company_email = company_email;
	}
	public String getCompany_phone() {
		return company_phone;
	}
	public void setCompany_phone(String company_phone) {
		this.company_phone = company_phone;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_storenum() {
		return company_storenum;
	}
	public void setCompany_storenum(String company_storenum) {
		this.company_storenum = company_storenum;
	}
	
}
